
public class Escape {

	public static void main(String[] args) {
		
		// 이스케이ㅡ문자 
		
		// \ n : 줄바꿈
		System.out.println("안녕하세요\n반갑\n습니다");
		
		// \t : 탭키 역할
		System.out.println("안녕하세요\n반갑\n습니다");
		
		// \" : 쌍따음표 출력 역할
		System.out.println("안녕하세요\"반갑습니다\"");
		
		// \' : 작은 따음표 출력 역할
		System.out.println("안녕하세요\n반갑\n습니다");

		// \\ :: 역슬래시를 그냥 출력할때
		System.out.println("C:\\Program file\\Neexn\\");


	}

}
